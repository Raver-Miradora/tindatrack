Future<void> seedDatabase(AppDatabase db) async {
  final count = await db.select(db.products).get();
  if (count.isNotEmpty) return;

  const uuid = Uuid();
  final now = DateTime.now();

  final List<ProductsCompanion> seeds = [];

  // Intelligent Category & Unit Guessing
  String guessCategory(String name) {
    final n = name.toLowerCase();
    if (n.contains('tuna') || n.contains('sardine') || n.contains('carne') || n.contains('corned') || n.contains('beefloaf') || n.contains('meatloaf') || n.contains('sausage') || n.contains('liverspread') || n.contains('mackerel') || n.contains('squid') || n.contains('maling') || n.contains('spam') || n.contains('luncheon') || n.contains('ulam')) return 'Canned Goods';
    if (n.contains('milk') || n.contains('krem') || n.contains('evap') || n.contains('condensed') || n.contains('cheese') || n.contains('chee') || n.contains('cream') || n.contains('nestogen') || n.contains('birchtree') || n.contains('bearbrand') || n.contains('alaska') || n.contains('jersey')) return 'Dairy';
    if (n.contains('coffee') || n.contains('nescafe') || n.contains('kopiko') || n.contains('great taste') || n.contains('san mig') || n.contains('milo') || n.contains('juice') || n.contains('chocolait') || n.contains('energen') || n.contains('coke') || n.contains('sprite') || n.contains('royal') || n.contains('pepsi') || n.contains('vida') || n.contains('moo') || n.contains('sting') || n.contains('tang')) return 'Beverages';
    if (n.contains('biscuit') || n.contains('bingo') || n.contains('fita') || n.contains('skyflakes') || n.contains('oreo') || n.contains('hansel') || n.contains('rebisco') || n.contains('bravo') || n.contains('fudgee') || n.contains('cupcake') || n.contains('inipit') || n.contains('lava') || n.contains('chips') || n.contains('piattos') || n.contains('mammon') || n.contains('otso') || n.contains('stick wafer') || n.contains('monde') || n.contains('cookies') || n.contains('cake bar') || n.contains('moby') || n.contains('snacku') || n.contains('cheese ring') || n.contains('nova') || n.contains('loaded')) return 'Snacks & Biscuits';
    if (n.contains('pancit') || n.contains('canton') || n.contains('lucky me') || n.contains('payless') || n.contains('noodle') || n.contains('instant')) return 'Noodles';
    if (n.contains('soysauce') || n.contains('vinegar') || n.contains('patis') || n.contains('catsup') || n.contains('ketchup') || n.contains('tomato') || n.contains('oyster') || n.contains('garbanzos') || n.contains('cornstarch') || n.contains('gawgaw') || n.contains('knorr') || n.contains('magic') || n.contains('mama sita') || n.contains('ajinomoto') || n.contains('peas') || n.contains('corn kernel') || n.contains('spread') || n.contains('mayonnaise') || n.contains('peanut butter') || n.contains('margarine') || n.contains('bagoong') || n.contains('sauce') || n.contains('gata') || n.contains('tomato paste')) return 'Condiments & Pantry';
    if (n.contains('shampoo') || n.contains('soap') || n.contains('safeguard') || n.contains('toothpaste') || n.contains('personal') || n.contains('colgate') || n.contains('palmolive') || n.contains('creamsilk') || n.contains('conditioner') || n.contains('head & shoulders') || n.contains('dove') || n.contains('close-up') || n.contains('perla')) return 'Personal Care';
    if (n.contains('powder') || n.contains('surf') || n.contains('tide') || n.contains('fabcon') || n.contains('household') || n.contains('ariel') || n.contains('downy') || n.contains('joy') || n.contains('detergent') || n.contains('insect') || n.contains('killer') || n.contains('protector') || n.contains('dishwashing') || n.contains('calla') || n.contains('champion') || n.contains('pride') || n.contains('bleach') || n.contains('zonrox')) return 'Household';
    if (n.contains('mentos') || n.contains('maxx') || n.contains('icool') || n.contains('vfresh') || n.contains('yakee') || n.contains('candy') || n.contains('bubblepop') || n.contains('champi') || n.contains('frutos') || n.contains('monami') || n.contains('jellee')) return 'Candy & Sweets';
    return 'Grocery';
  }

  String guessUnit(String name) {
    final n = name.toLowerCase();
    if (n.contains('sachet') || n.contains('stick') || n.contains('twinpack') || n.contains('pack')) return 'sachet';
    if (n.contains('case')) return 'case';
    if (n.contains('can') || n.contains('g') || n.contains('ml')) return 'can';
    if (n.contains('bottle') || n.contains('liter')) return 'bottle';
    if (n.contains('pouch') || n.contains('bag')) return 'pouch';
    return 'pcs';
  }

  final Set<String> seenBarcodes = {};

  for (final item in [...batch2SeedData, ...batch1SeedData, ...sariSariSeedData]) {
    final barcode = item['barcode'] as String;
    if (seenBarcodes.contains(barcode)) continue;
    seenBarcodes.add(barcode);

    final price = (item['price'] as num).toDouble();
    final cost = item.containsKey('cost') 
        ? (item['cost'] as num).toDouble() 
        : price * 0.8;

    seeds.add(
      ProductsCompanion.insert(
        id: uuid.v4(),
        name: item['name']!,
        barcode: Value(barcode),
        category: guessCategory(item['name']!),
        unit: guessUnit(item['name']!),
        reorderPoint: 10.0,
        averageCost: Value(cost),
        averageSellingPrice: Value(price),
        currentStock: const Value(0.0),
        updatedAt: Value(now),
      )
    );
  }

  // Use batch with conflict resolution
  await db.batch((batch) {
    batch.insertAll(db.products, seeds, mode: InsertMode.insertOrReplace);
  });

  // Seed initial Audit Log
  await db.into(db.auditLog).insert(
    AuditLogCompanion.insert(
      id: uuid.v4(),
      actionType: 'Master Catalog Seeded',
      targetType: 'database',
      targetId: 'master_seed',
      timestamp: Value(now),
    ),
  );
}

